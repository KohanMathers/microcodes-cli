public class example {
    static int add(int x, int y) {
        return x + y;
    }

    public static void main(String[] args) {
        int a = 5;
        int b = 10;
        int result = add(a, b);
        System.out.println("Hello, World!");
        System.out.println("The result of adding " + a + " and " + b + " is: " + result);
    }
}
